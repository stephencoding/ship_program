const buildTrajectory = (vessel: Vessel): TrajectoryPoint[] => {
  const offsets = [
    [-0.18, -0.22],
    [-0.12, -0.16],
    [-0.07, -0.1],
    [-0.03, -0.04],
    [0, 0],
  ];

  return offsets.map(([latOffset, lngOffset], index) => ({
    lat: Number((vessel.lat + latOffset).toFixed(6)),
    lng: Number((vessel.lng + lngOffset).toFixed(6)),
    course: vessel.course,
    speed: Math.max(0, Number((vessel.speed - (4 - index) * 0.7).toFixed(1))),
    timestamp: `2026-05-22 ${String(6 + index).padStart(2, '0')}:30`,
  }));
};

